function CalendarView({ meetings, t, onSelectMeeting, lang }) {
  const today = new Date();
  const [viewMode, setViewMode] = useState('month');
  const [currentDate, setCurrentDate] = useState(new Date(today.getFullYear(), today.getMonth(), 1));
  const [selectedDay, setSelectedDay] = useState(null);
  const locale = lang === 'de' ? 'de-DE' : 'en-US';

  const meetingsByDate = {};
  meetings.forEach(m => {
    const d = new Date(m.meetingDate);
    const key = `${d.getFullYear()}-${d.getMonth()}-${d.getDate()}`;
    if (!meetingsByDate[key]) meetingsByDate[key] = [];
    meetingsByDate[key].push(m);
  });

  const getMeetingsForDay = (year, month, day) => meetingsByDate[`${year}-${month}-${day}`] || [];

  const navigateMonth = (dir) => { setCurrentDate(prev => new Date(prev.getFullYear(), prev.getMonth() + dir, 1)); setSelectedDay(null); };
  const navigateYear = (dir) => { setCurrentDate(prev => new Date(prev.getFullYear() + dir, prev.getMonth(), 1)); setSelectedDay(null); };

  const renderMonthView = () => {
    const year = currentDate.getFullYear();
    const month = currentDate.getMonth();
    const firstDay = new Date(year, month, 1).getDay();
    const daysInMonth = new Date(year, month + 1, 0).getDate();
    const startOffset = firstDay === 0 ? 6 : firstDay - 1;
    const dayNames = lang === 'de' ? ['Mo', 'Di', 'Mi', 'Do', 'Fr', 'Sa', 'So'] : ['Mon', 'Tue', 'Wed', 'Thu', 'Fri', 'Sat', 'Sun'];
    const cells = [];
    for (let i = 0; i < startOffset; i++) cells.push(null);
    for (let d = 1; d <= daysInMonth; d++) cells.push(d);
    const monthName = currentDate.toLocaleDateString(locale, { month: 'long', year: 'numeric' });
    const selectedMeetings = selectedDay ? getMeetingsForDay(year, month, selectedDay) : [];

    return (
      <div className="cal-month-wrapper">
        <div className="cal-month-grid">
          <div className="cal-header">
            <button className="cal-nav-btn" onClick={() => navigateMonth(-1)}>‹</button>
            <h3 className="cal-title">{monthName}</h3>
            <button className="cal-nav-btn" onClick={() => navigateMonth(1)}>›</button>
          </div>
          <div className="cal-day-names">{dayNames.map(d => <div key={d} className="cal-day-name">{d}</div>)}</div>
          <div className="cal-grid">
            {cells.map((day, i) => {
              if (!day) return <div key={`empty-${i}`} className="cal-cell cal-cell-empty" />;
              const dayMeetings = getMeetingsForDay(year, month, day);
              const isToday = today.getFullYear() === year && today.getMonth() === month && today.getDate() === day;
              const isSelected = selectedDay === day;
              return (
                <div key={day} className={`cal-cell ${isToday ? 'cal-today' : ''} ${isSelected ? 'cal-selected' : ''} ${dayMeetings.length > 0 ? 'cal-has-meetings' : ''}`}
                  onClick={() => setSelectedDay(isSelected ? null : day)}>
                  <span className="cal-day-num">{day}</span>
                  {dayMeetings.length > 0 && (
                    <div className="cal-dots">
                      {dayMeetings.slice(0, 3).map((m, idx) => (
                        <span key={idx} className={`cal-dot ${m.aiSummary ? 'dot-analyzed' : m.protocolText ? 'dot-open' : 'dot-draft'}`} />
                      ))}
                      {dayMeetings.length > 3 && <span className="cal-dot-more">+{dayMeetings.length - 3}</span>}
                    </div>
                  )}
                </div>
              );
            })}
          </div>
        </div>
        <div className="cal-day-detail">
          {selectedDay ? (
            <>
              <h3 className="cal-detail-title">
                {t.meetingsOnDay} {new Date(currentDate.getFullYear(), currentDate.getMonth(), selectedDay).toLocaleDateString(locale, { weekday: 'long', day: 'numeric', month: 'long' })}
              </h3>
              {selectedMeetings.length === 0 ? (
                <div className="cal-no-meetings"><div style={{ fontSize: 32, marginBottom: 8 }}>📅</div><p>{t.noMeetingsOnDay}</p></div>
              ) : (
                <div className="cal-meeting-list">
                  {selectedMeetings.map(m => (
                    <div key={m.id} className="cal-meeting-item" onClick={() => onSelectMeeting(m)}>
                      <div className="cal-meeting-time">{new Date(m.meetingDate).toLocaleTimeString(locale, { hour: '2-digit', minute: '2-digit' })}</div>
                      <div className="cal-meeting-info">
                        <span className="cal-meeting-title">{m.title}</span>
                        {m.location && <span className="cal-meeting-loc">{m.location}</span>}
                      </div>
                      <StatusBadge meeting={m} t={t} />
                    </div>
                  ))}
                </div>
              )}
            </>
          ) : (
            <div className="cal-no-meetings"><div style={{ fontSize: 32, marginBottom: 8 }}>👆</div><p>{t.selectDay}</p></div>
          )}
        </div>
      </div>
    );
  };

  const renderYearView = () => {
    const year = currentDate.getFullYear();
    const monthNames = Array.from({ length: 12 }, (_, i) => new Date(year, i, 1).toLocaleDateString(locale, { month: 'long' }));
    return (
      <div className="cal-year-wrapper">
        <div className="cal-header" style={{ background: 'transparent', border: 'none', padding: '0 0 16px 0' }}>
          <button className="cal-nav-btn" onClick={() => navigateYear(-1)}>‹</button>
          <h3 className="cal-title">{year}</h3>
          <button className="cal-nav-btn" onClick={() => navigateYear(1)}>›</button>
        </div>
        <div className="cal-year-grid">
          {monthNames.map((monthName, monthIdx) => {
            const firstDay = new Date(year, monthIdx, 1).getDay();
            const daysInMonth = new Date(year, monthIdx + 1, 0).getDate();
            const startOffset = firstDay === 0 ? 6 : firstDay - 1;
            const cells = [];
            for (let i = 0; i < startOffset; i++) cells.push(null);
            for (let d = 1; d <= daysInMonth; d++) cells.push(d);
            const monthMeetings = meetings.filter(m => { const d = new Date(m.meetingDate); return d.getFullYear() === year && d.getMonth() === monthIdx; });
            return (
              <div key={monthIdx} className="cal-mini-month" onClick={() => { setCurrentDate(new Date(year, monthIdx, 1)); setViewMode('month'); setSelectedDay(null); }}>
                <div className="cal-mini-header">
                  <span className="cal-mini-title">{monthName}</span>
                  {monthMeetings.length > 0 && <span className="cal-mini-count">{monthMeetings.length}</span>}
                </div>
                <div className="cal-mini-grid">
                  {cells.map((day, i) => {
                    if (!day) return <div key={`e-${i}`} className="cal-mini-cell" />;
                    const dayMeetings = getMeetingsForDay(year, monthIdx, day);
                    const isToday = today.getFullYear() === year && today.getMonth() === monthIdx && today.getDate() === day;
                    return (
                      <div key={day} className={`cal-mini-cell ${dayMeetings.length > 0 ? 'cal-mini-has-meeting' : ''} ${isToday ? 'cal-mini-today' : ''}`}>{day}</div>
                    );
                  })}
                </div>
              </div>
            );
          })}
        </div>
      </div>
    );
  };

  return (
    <div className="detail-panel fade-in">
      <div className="detail-header">
        <div><h2>{t.calendar}</h2><p className="detail-sub">{t.calendarSub}</p></div>
        <div className="detail-actions">
          <button className={`filter-chip ${viewMode === 'month' ? 'active' : ''}`} style={{ padding: '8px 16px' }} onClick={() => setViewMode('month')}>{t.monthView}</button>
          <button className={`filter-chip ${viewMode === 'year' ? 'active' : ''}`} style={{ padding: '8px 16px' }} onClick={() => setViewMode('year')}>{t.yearView}</button>
        </div>
      </div>
      <div className="cal-body">{viewMode === 'month' ? renderMonthView() : renderYearView()}</div>
    </div>
  );
}

