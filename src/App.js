function groupMeetingsByDate(meetings, t) {
  const today = new Date();
  today.setHours(0, 0, 0, 0);
  const weekAgo = new Date(today);
  weekAgo.setDate(today.getDate() - 7);
  const todayLabel = t.dashboard === 'Dashboard' ? 'Heute' : 'Today';
  const weekLabel = t.dashboard === 'Dashboard' ? 'Diese Woche' : 'This Week';
  const olderLabel = t.dashboard === 'Dashboard' ? 'Älter' : 'Older';
  const groups = { [todayLabel]: [], [weekLabel]: [], [olderLabel]: [] };
  meetings.forEach(m => {
    const d = new Date(m.meetingDate);
    d.setHours(0, 0, 0, 0);
    if (d.getTime() === today.getTime()) groups[todayLabel].push(m);
    else if (d >= weekAgo) groups[weekLabel].push(m);
    else groups[olderLabel].push(m);
  });
  return groups;
}

function applyFilterAndSort(meetings, search, filter, sort) {
  let result = [...meetings];
  if (search) {
    const q = search.toLowerCase();
    result = result.filter(m =>
      m.title.toLowerCase().includes(q) ||
      (m.protocolText && m.protocolText.toLowerCase().includes(q)) ||
      (m.location && m.location.toLowerCase().includes(q))
    );
  }
  if (filter === 'analyzed') result = result.filter(m => m.aiSummary);
  if (filter === 'open') result = result.filter(m => m.protocolText && !m.aiSummary);
  if (filter === 'draft') result = result.filter(m => !m.protocolText);
  result.sort((a, b) => {
    if (sort === 'date-desc') return new Date(b.meetingDate) - new Date(a.meetingDate);
    if (sort === 'date-asc') return new Date(a.meetingDate) - new Date(b.meetingDate);
    if (sort === 'title-asc') return a.title.localeCompare(b.title);
    if (sort === 'title-desc') return b.title.localeCompare(a.title);
    return 0;
  });
  return result;
}

async function exportToPDF(meeting) {
  const element = document.getElementById('export-content');
  element.classList.add('exporting');
  const canvas = await html2canvas(element, { scale: 2, useCORS: true });
  element.classList.remove('exporting');
  const imgData = canvas.toDataURL('image/png');
  const pdf = new jsPDF('p', 'mm', 'a4');
  const width = pdf.internal.pageSize.getWidth();
  const height = (canvas.height * width) / canvas.width;
  pdf.addImage(imgData, 'PNG', 0, 0, width, height);
  pdf.save(`${meeting.title.replace(/\s+/g, '_')}_Protokoll.pdf`);
}

function useSpeechRecognition(onTranscript, lang) {
  const recognitionRef = useRef(null);
  const [listening, setListening] = useState(false);
  const [supported, setSupported] = useState(false);
  useEffect(() => {
    const SpeechRecognition = window.SpeechRecognition || window.webkitSpeechRecognition;
    if (SpeechRecognition) {
      setSupported(true);
      const recognition = new SpeechRecognition();
      recognition.lang = lang === 'en' ? 'en-US' : 'de-DE';
      recognition.continuous = true;
      recognition.interimResults = true;
      recognition.onresult = (e) => {
        let final = '';
        for (let i = e.resultIndex; i < e.results.length; i++) {
          if (e.results[i].isFinal) final += e.results[i][0].transcript + ' ';
        }
        if (final) onTranscript(final);
      };
      recognition.onend = () => setListening(false);
      recognitionRef.current = recognition;
    }
  }, [lang]);
  const startListening = () => { if (recognitionRef.current && !listening) { recognitionRef.current.start(); setListening(true); } };
  const stopListening = () => { if (recognitionRef.current && listening) { recognitionRef.current.stop(); setListening(false); } };
  return { listening, supported, startListening, stopListening };
}

