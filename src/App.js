function ParticipantInput({ participants, setParticipants, t }) {
  const [participantInput, setParticipantInput] = useState('');
  const addParticipant = () => {
    const name = participantInput.trim();
    if (name && !participants.includes(name)) { setParticipants([...participants, name]); setParticipantInput(''); }
  };
  return (
    <div>
      <div className="participant-input-row">
        <input placeholder={t.participantPlaceholder} value={participantInput} onChange={e => setParticipantInput(e.target.value)} onKeyDown={e => e.key === 'Enter' && addParticipant()} />
        <button type="button" className="btn-add-participant" onClick={addParticipant}>+</button>
      </div>
      {participants.length > 0 && (
        <div className="participant-chips">
          {participants.map(p => <span key={p} className="participant-chip">{p}<button onClick={() => setParticipants(participants.filter(x => x !== p))}>×</button></span>)}
        </div>
      )}
    </div>
  );
}

