function WeeklyChart({ meetings, t }) {
  const weeks = {};
  meetings.forEach(m => {
    const d = new Date(m.meetingDate);
    const year = d.getFullYear();
    const week = Math.ceil(((d - new Date(year, 0, 1)) / 86400000 + new Date(year, 0, 1).getDay() + 1) / 7);
    const key = `${t.weekLabel} ${week}`;
    weeks[key] = (weeks[key] || 0) + 1;
  });
  const entries = Object.entries(weeks).slice(-6);
  const max = Math.max(...entries.map(([, v]) => v), 1);
  if (entries.length === 0) return <div className="chart-empty"><p>{t.dashboard === 'Dashboard' ? 'Noch keine Daten für das Diagramm.' : 'No data for the chart yet.'}</p></div>;
  return (
    <div className="weekly-chart">
      <div className="chart-bars">
        {entries.map(([label, count]) => (
          <div key={label} className="chart-bar-col">
            <span className="chart-bar-value">{count}</span>
            <div className="chart-bar-track"><div className="chart-bar-fill" style={{ height: `${(count / max) * 100}%` }} /></div>
            <span className="chart-bar-label">{label}</span>
          </div>
        ))}
      </div>
    </div>
  );
}

